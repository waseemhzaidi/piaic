/** Favourite Fruits */

var favorite_fruits=["apple","banana","orange","mango","peach","lychee","plum"];
let idx=0;

while(idx<favorite_fruits.length){
  if(favorite_fruits[idx]==="plum") 
    console.log("You really like plum");
  if(favorite_fruits[idx]==="lychee") 
    console.log("You really like lychee");
  if(favorite_fruits[idx]==="apple") 
    console.log("You really like apple");
  if(favorite_fruits[idx]==="orange") 
    console.log("You really like orange");
  if(favorite_fruits[idx]==="banana") 
    console.log("You really like banana");

  idx++;
}